class Detalle{
    detalle_id = 0;
    detalle_cant = 0;
    detalle_desc = "";
    detalle_p_unit = 0;

    constructor(detalle_id, detalle_cant, detalle_desc, detalle_p_unit){
        this.detalle_id = detalle_id;
        this.detalle_cant = detalle_cant;
        this.detalle_desc = detalle_desc;
        this.detalle_p_unit = detalle_p_unit;
    }
}